#!/bin/bash
#|#./ingredients/fonts/kitty-jetbrains-mono.sh #JetBrains Mono font for Kitty terminal emulator [Requires /system/kitty and /fonts/0_xxx-fonts ingredients]

echo "font_family       JetBrains Mono" >> ~/.config/kitty/kitty.conf
echo "bold_font         auto" >> ~/.config/kitty/kitty.conf
echo "italic_font       auto" >> ~/.config/kitty/kitty.conf
echo "bold_italic_font  auto" >> ~/.config/kitty/kitty.conf
echo "font_size         12.0" >> ~/.config/kitty/kitty.conf
echo "" >> ~/.config/kitty/kitty.conf
